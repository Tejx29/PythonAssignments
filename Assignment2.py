# comparision operators / Relational operators
a=10
b=5
print(">=",a>=b)
print("<=",a<=b)
print(">",a>b)
print("<",a<b)
print("==",a==b)
print("!=",a!=b)

#logical operators
a=True
b=False
print("and",a and b)
print("or",a or b)
print("not",not b)

#bitwise operators
a=10
b=5
print("bitwise &",a&b)
print("bitwise |",a|b)
print("bitwise ^",a^b)
print("bitwise >>",a>>b)
print("bitwise <<",a<<b)


#identity operators
a=10
b=5
print("is",a is b)
print("is not",a is not b)

#membership operators
a="tejas"
print('t' in a)
print("k" not in a)

