import math

class MathOperations:
    def sqrt(x):
        return (math.sqrt(x))

    def factorial(x):
        return (math.factorial(x))

    def sin(x):
        return (math.sin(x))

    def cos(x):
        return (math.cos(x))
    
    def tan(x):
        return (math.tan(x))

    def log2(x):
        return (math.log2(x))
    
